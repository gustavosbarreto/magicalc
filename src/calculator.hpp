#ifndef _CALCULATOR_HPP
#define _CALCULATOR_HPP

#include <QtGlobal>

qreal plusOperation(qreal x, qreal y) { return x + y; }
qreal minusOperation(qreal x, qreal y) { return x - y; }
qreal multiplyOperation(qreal x, qreal y) { return x * y; }
qreal divideOperation(qreal x, qreal y) { return x / y; }

#endif
